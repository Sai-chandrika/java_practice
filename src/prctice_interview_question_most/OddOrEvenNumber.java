package prctice_interview_question_most;

public class OddOrEvenNumber {
    public static void main(String[] args) {
        System.out.println(" print odd numbers");
        for(int i=0;i<=20; i++){
            if(i%2!=0){
                System.out.println(i);
            }
        }

        System.out.println(" print even numbers");
        for(int i=0;i<=20; i++){
            if(i%2==0){
                System.out.println(i);
            }
        }

    }

}
